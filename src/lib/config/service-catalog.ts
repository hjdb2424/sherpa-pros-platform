/** Get all category IDs for use in dropdowns */
export const CATEGORY_IDS = SERVICE_CATALOG.map((c) => c.id);

/** Flat list of all sub-services with parent category */
export const ALL_SUB_SERVICES = SERVICE_CATALOG.flatMap((cat) =>
  cat.subServices.map((sub) => ({
    ...sub,
    categoryId: cat.id,
    categoryName: cat.name,
  })),
);

/** Lookup a category by ID */
export function getCategory(id: string): ServiceCategory | undefined {
  return SERVICE_CATALOG.find((c) => c.id === id);
}

/** Lookup a sub-service by ID (searches all categories) */
export function getSubService(id: string) {
  return ALL_SUB_SERVICES.find((s) => s.id === id);
}

/** Get sub-services filtered by urgency */
export function getEmergencyServices() {
  return ALL_SUB_SERVICES.filter((s) => s.urgency === 'emergency');
}

/** Get all categories as select options */
export function getCategoryOptions() {
  return SERVICE_CATALOG.map((c) => ({
    value: c.id,
    label: c.name,
    icon: c.icon,
  }));
}

/** Get categories by regional tag */
export function getCategoriesByRegion(region: RegionalTag) {
  return SERVICE_CATALOG.filter(
    (c) => c.regional?.includes(region) || c.regional?.includes('national'),
  );
}

/** Get all regional tags in use */
export function getAvailableRegions(): RegionalTag[] {
  const regions = new Set<RegionalTag>();
  SERVICE_CATALOG.forEach((c) => c.regional?.forEach((r) => regions.add(r)));
  return Array.from(regions);
}

/** Total counts for catalog stats */
export const CATALOG_STATS = {
  totalCategories: SERVICE_CATALOG.length,
  totalSubServices: ALL_SUB_SERVICES.length,
  emergencyServices: ALL_SUB_SERVICES.filter((s) => s.urgency === 'emergency').length,
  regionalCategories: SERVICE_CATALOG.filter(
    (c) => c.regional && !c.regional.includes('national'),
  ).length,
} as const;

/** Get average Wiseman confidence for a category */
export function getCategoryConfidence(categoryId: string): number {
  const category = SERVICE_CATALOG.find((c) => c.id === categoryId);
  if (!category || category.subServices.length === 0) return 0;
  const total = category.subServices.reduce((sum, s) => sum + s.wisemanConfidence, 0);
  return Math.round(total / category.subServices.length);
}

/** Get primary Wiseman source for a category */
export function getCategoryWisemanSource(categoryId: string): string {
  const category = SERVICE_CATALOG.find((c) => c.id === categoryId);
  if (!category) return '';
  return category.subServices[0]?.wisemanSource ?? '';
}
